fn generate_artifact(dir: &Path, scenario: &VerificationScenario) -> io::Result<()> {
    let stem = scenario.id;
    let path = dir.join(format!("{}.csv", stem));
    let det_path = dir.join(format!("{}.det.csv", stem));
    let bin_path = dir.join(format!("{}_headers.bin", stem));
    let canon_path = dir.join(format!("{}.canon.sig", stem));

    let mut file = io::BufWriter::new(File::create(&path)?);
    let mut det_file = io::BufWriter::new(File::create(&det_path)?);
    let mut bin_file = io::BufWriter::new(File::create(&bin_path)?);
    let mut canon_file = io::BufWriter::new(File::create(&canon_path)?);

    writeln!(
        file,
        "# Precision-DPW v{} Reference Finalization",
        env!("CARGO_PKG_VERSION")
    )?;
    writeln!(file, "# Target: {}", scenario.target_desc)?;
    writeln!(file, "# SR: {}", scenario.sample_rate)?;
    if scenario.shape == 10 {
        writeln!(
            file,
            "# Columns: tick, saw_s32, pulse_s32, tri_s32, sine_s32, saw_phase_ref, phase_residual, sine_ref, sine_residual, raw_u64_phase"
        )?;
        writeln!(
            file,
            "tick, saw_s32, pulse_s32, tri_s32, sine_s32, saw_phase_ref, phase_residual, sine_ref, sine_residual, raw_u64_phase"
        )?;
        writeln!(
            det_file,
            "tick,saw_s32,pulse_s32,tri_s32,sine_s32,phase_u32_hex"
        )?;
    } else {
        writeln!(file, "# Columns: tick, sample_i32, i128_scaled, f64_ref, internal_residual, clipping_active, current_exponent, raw_u64_phase")?;
        writeln!(file, "tick, sample_i32, i128_scaled, f64_ref, internal_residual, clipping_active, current_exponent, raw_u64_phase")?;
        writeln!(det_file, "tick,sample_i32,raw_i128_hex,phase_u32_hex,shape")?;
    }

    let mut hasher = Sha256::new();

    let mut state_i128 = OscState::new();
    state_i128.reset();

    let mut ref_a = Dpw4StateF64::new();
    let mut ref_b = Dpw4StateF64::new();
    ref_a.reset();
    ref_b.reset();

    let gain_i128 = quick_validate_gain_for_scenario(scenario);

    let duty_scalar = Scalar::from_num(scenario.duty_pct / 100.0);
    state_i128.duty = duty_scalar;

    let mut current_phase = Scalar::ZERO;
    let freq_scalar = Scalar::from_num(scenario.freq);
    let rate_scalar = Scalar::from_num(scenario.sample_rate);
    let phase_inc_scalar = (freq_scalar / rate_scalar) * math::TWO_PI;
    let float_gain = libm::pow(2.0, scenario.gain_exponent as f64);

    let chirp_f0 = Scalar::from_num(20.0);
    let chirp_f1 = Scalar::from_num(20000.0);
    let chirp_step = if scenario.num_samples > 1 {
        (chirp_f1 - chirp_f0) / Scalar::from_num((scenario.num_samples - 1) as f64)
    } else {
        Scalar::ZERO
    };

    let mut sweep_saw = OscState::new();
    let mut sweep_pulse = OscState::new();
    let mut sweep_tri = OscState::new();
    let mut sweep_sine = OscState::new();
    sweep_saw.reset();
    sweep_pulse.reset();
    sweep_tri.reset();
    sweep_sine.reset();
    sweep_pulse.duty = Scalar::from_num(0.5);

    let sweep_f0_f64 = 20.0f64;
    let sweep_f1_f64 = 20000.0f64;
    let sweep_k_f64 = if scenario.num_samples > 1 {
        (sweep_f1_f64 - sweep_f0_f64) / ((scenario.num_samples - 1) as f64)
    } else {
        0.0
    };

    for tick in 0..scenario.num_samples {
        if scenario.shape == 10 {
            let phase_u32 =
                (current_phase / math::TWO_PI * Scalar::from_num(4294967296.0)).to_num::<u32>();

            let saw_s32 = Sawtooth::tick(&mut sweep_saw, current_phase, &gain_i128);
            let pulse_s32 = Pulse::tick(&mut sweep_pulse, current_phase, &gain_i128);
            let tri_s32 = TriangleDPW4::tick(&mut sweep_tri, current_phase, &gain_i128);
            let sine_s32 = Sine::tick(&mut sweep_sine, current_phase, &gain_i128);

            // Canonical Hashing: Master Order [saw, pulse, triangle, sine]
            // Defined by DEBT-03 Normative Channel Order
            if scenario.channels == CanonChannels::MasterTuple {
                hasher.update(&saw_s32.to_le_bytes());
                hasher.update(&pulse_s32.to_le_bytes());
                hasher.update(&tri_s32.to_le_bytes());
                hasher.update(&sine_s32.to_le_bytes());
            } else {
                // If we get here with Mono shape=10, it's a config error, but we hash mono sample
                // However shape=10 logic is different.
                // For now, shape 10 IS MasterTuple.
                // We strictly follow channels enum for hashing.
            }

            // Golden chirp reference for the phase engine (Saw channel only).
            let n = tick as f64;
            let phase_ref = 2.0
                * core::f64::consts::PI
                * ((sweep_f0_f64 * n) / scenario.sample_rate
                    + 0.5 * sweep_k_f64 * n * n / scenario.sample_rate);
            let phase_ref_01 = phase_ref / (2.0 * core::f64::consts::PI);
            let saw_ref = 2.0 * (phase_ref_01 - libm::floor(phase_ref_01)) - 1.0;
            let phase_from_scalar_01 = (current_phase / math::TWO_PI).to_num::<f64>();
            let saw_from_scalar = 2.0 * phase_from_scalar_01 - 1.0;
            let phase_residual = saw_from_scalar - saw_ref;
            let sine_ref = libm::sin(phase_ref);
            let sine_norm = (sine_s32 as f64) / (i32::MAX as f64);
            let sine_residual = sine_norm - sine_ref;

            writeln!(
                file,
                "{}, {}, {}, {}, {}, {:.18e}, {:.18e}, {:.18e}, {:.18e}, {:.18e}",
                tick,
                saw_s32,
                pulse_s32,
                tri_s32,
                sine_s32,
                saw_ref,
                phase_residual,
                sine_ref,
                sine_residual,
                current_phase.to_num::<f64>()
            )?;
            writeln!(
                det_file,
                "{},{},{},{},{},0x{:08x}",
                tick, saw_s32, pulse_s32, tri_s32, sine_s32, phase_u32
            )?;

            let header = SignalFrameHeader::new(tick as u64, scenario.sample_rate as u32);
            bin_file.write_all(&header.to_bytes())?;

            let inst_freq = chirp_f0 + chirp_step * Scalar::from_num(tick as f64);
            let phase_inc_chirp = (inst_freq / rate_scalar) * math::TWO_PI;
            current_phase += phase_inc_chirp;
            if current_phase >= math::TWO_PI {
                current_phase -= math::TWO_PI;
            }
            continue;
        }

        let (sample_i32, raw_i128) = if scenario.shape == 1 || scenario.shape == 3 {
            let duty_val = if scenario.shape == 3 {
                Scalar::from_num(0.5)
            } else {
                state_i128.duty
            };
            let duty_ph = duty_val * math::TWO_PI;

            let to_q31 = |mut p: Scalar| {
                p %= math::TWO_PI;
                if p < Scalar::ZERO {
                    p += math::TWO_PI;
                }
                let u32_phase = (p / math::TWO_PI * Scalar::from_num(4294967296.0)).to_num::<u32>();
                (u32_phase as i64).wrapping_sub(1i64 << 31)
            };

            let s_a_q31 = to_q31(current_phase);
            let raw_a = dpw4::tick_dpw4_raw(&mut state_i128.saw_a, s_a_q31);

            let s_b_q31 = to_q31(current_phase - duty_ph);
            let raw_b = dpw4::tick_dpw4_raw(&mut state_i128.saw_b, s_b_q31);

            let raw_diff = raw_a.wrapping_sub(raw_b);
            let raw_mix = raw_diff >> 1;
            let sample = apply_gain(raw_mix, gain_i128.m4_q63, gain_i128.e4);
            (sample, raw_diff)
        } else if scenario.shape == 2 {
            let sample = TriangleDPW4::tick(&mut state_i128, current_phase, &gain_i128);
            (sample, sample as i128)
        } else if scenario.shape == 4 {
            let sample = Sine::tick(&mut state_i128, current_phase, &gain_i128);
            (sample, sample as i128)
        } else {
            let mut p = current_phase % math::TWO_PI;
            if p < Scalar::ZERO {
                p += math::TWO_PI;
            }
            let u32_phase = (p / math::TWO_PI * Scalar::from_num(4294967296.0)).to_num::<u32>();
            let s_q31 = (u32_phase as i64).wrapping_sub(1i64 << 31);
            let raw4 = dpw4::tick_dpw4_raw(&mut state_i128.saw_a, s_q31);
            let sample = apply_gain(raw4, gain_i128.m4_q63, gain_i128.e4);
            (sample, raw4)
        };

        // Canonical Hashing: Mono [sample]
        if scenario.channels == CanonChannels::Mono {
            hasher.update(&sample_i32.to_le_bytes());
        }

        let ph_01 = (current_phase / math::TWO_PI).to_num::<f64>();

        let out_ref = if scenario.shape == 1 || scenario.shape == 3 {
            let duty_f = scenario.duty_pct / 100.0;
            let sa = tick_dpw4_f64(&mut ref_a, ph_01);

            let mut ph_b = ph_01 - duty_f;
            if ph_b < 0.0 {
                ph_b += 1.0;
            }
            let sb = tick_dpw4_f64(&mut ref_b, ph_b);
            (sa - sb) * 0.5 * float_gain
        } else if scenario.shape == 2 {
            // Ideal triangle reference: starts at 0, rises to +1, then falls to -1.
            let tri = 4.0 * libm::fabs(ph_01 - libm::floor(ph_01 + 0.75) + 0.25) - 1.0;
            tri * float_gain
        } else if scenario.shape == 4 {
            libm::sin(current_phase.to_num::<f64>()) * float_gain
        } else {
            tick_dpw4_f64(&mut ref_a, ph_01) * float_gain
        };

        let scaling_factor = if scenario.shape == 1 || scenario.shape == 3 {
            0.5
        } else {
            1.0
        };
        let i128_scaled = if scenario.shape == 2 || scenario.shape == 4 {
            (sample_i32 as f64) / (i32::MAX as f64)
        } else {
            (raw_i128 as f64) * Q124_LSB * scaling_factor * float_gain * C4
        };
        let residual = i128_scaled - out_ref;
        let clipping_active = if libm::fabs(i128_scaled) > 1.0 { 1 } else { 0 };

        writeln!(
            file,
            "{}, {}, {:.18e}, {:.18e}, {:.18e}, {}, {}, {:.18e}",
            tick,
            sample_i32,
            i128_scaled,
            out_ref,
            residual,
            clipping_active,
            scenario.gain_exponent,
            current_phase.to_num::<f64>()
        )?;
        let phase_u32 =
            (current_phase / math::TWO_PI * Scalar::from_num(4294967296.0)).to_num::<u32>();
        writeln!(
            det_file,
            "{},{},0x{:032x},0x{:08x},{}",
            tick, sample_i32, raw_i128 as u128, phase_u32, scenario.shape
        )?;

        let header = SignalFrameHeader::new(tick as u64, scenario.sample_rate as u32);
        bin_file.write_all(&header.to_bytes())?;

        current_phase += phase_inc_scalar;
        if current_phase >= math::TWO_PI {
            current_phase -= math::TWO_PI;
        }
    }

    let hash_string = hex::encode(hasher.finalize());
    let channels_str = match scenario.channels {
        CanonChannels::Mono => "mono",
        CanonChannels::MasterTuple => "saw,pulse,triangle,sine",
    };
    writeln!(
        canon_file,
        "v1 | le-i32 | channels={} | samples={}",
        channels_str, scenario.num_samples
    )?;
    writeln!(canon_file, "{}", hash_string)?;

    Ok(())
}

// --- COMMAND: GENERATE ---
fn run_generate(args: GenerateArgs) -> io::Result<()> {
    // Validation: WAV requires fixed duration
    if args.container_wav && args.seconds.is_none() {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "Error: --container-wav requires --seconds to be set.",
        ));
    }

    // DSP Setup
    let mut osc = OscState::new();
    let freq_scalar = Scalar::from_num(args.freq);
    let rate_scalar = Scalar::from_num(args.rate);
    let two_pi_scalar = math::TWO_PI;
    let phase_inc = (freq_scalar / rate_scalar) * two_pi_scalar;

    // Gain Calculation (dB -> Scalar)
    let linear = libm::pow(10.0, args.gain / 20.0);
    // Base gain (assuming Sawtooth/delta^3 normalization or consistent usage)
    let mut gain_f64 = linear * (1u64 << 63) as f64;
    let mut gain_exp = 0;

    // Normalize to u64 range to prevent overflow
    // We keep the mantissa within u64 range by shifting to exponent
    // Note: gain_exp is applied via DpwGain exponent semantics (see apply_gain / DpwGain docs).
    while gain_f64 >= 18446744073709551616.0 {
        // 2^64
        gain_f64 *= 0.5;
        gain_exp += 1;
    }

    let gain = DpwGain::new(gain_f64 as u64, gain_exp, 0, 0);

    let stdout = io::stdout();
    let mut handle = stdout.lock();

    // Write Header (WAV or DP32)
    if args.container_wav {
        let seconds = args.seconds.ok_or_else(|| {
            io::Error::new(
                io::ErrorKind::InvalidInput,
                "--container-wav requires --seconds",
            )
        })?;
        let total_samples = args.rate as u64 * seconds;
        let data_bytes = total_samples * 4;
        write_wav_header(&mut handle, args.rate, data_bytes as u32)?;
    } else {
        let header = SignalFrameHeader::new(0, args.rate);
        handle.write_all(&header.to_bytes())?;
    }

    // Stream Loop
    let mut buffer = [0i32; 512];
    let mut phases = [Scalar::ZERO; 512];
    let mut current_phase = Scalar::ZERO;
    let mut samples_remaining = args
        .seconds
        .map(|s| s * args.rate as u64)
        .unwrap_or(u64::MAX);

    while samples_remaining > 0 {
        let chunk_size = std::cmp::min(buffer.len() as u64, samples_remaining) as usize;

        for i in 0..chunk_size {
            phases[i] = current_phase;
            current_phase += phase_inc;
            if current_phase >= two_pi_scalar {
                current_phase -= two_pi_scalar;
            }
        }

        match args.shape {
            ShapeArg::Saw => signal_pipe::<Sawtooth>(
                &mut osc,
                &phases[0..chunk_size],
                &gain,
                &mut buffer[0..chunk_size],
            ),
            ShapeArg::Square => signal_pipe::<Square>(
                &mut osc,
                &phases[0..chunk_size],
                &gain,
                &mut buffer[0..chunk_size],
            ),
            ShapeArg::Triangle => {
                // DPW4 Triangle (Production)
                signal_pipe::<TriangleDPW4>(
                    &mut osc,
                    &phases[0..chunk_size],
                    &gain,
                    &mut buffer[0..chunk_size],
                );
            }
            ShapeArg::TriangleDpw1 => {
                // Naive Triangle (Baseline/Forensic)
                if samples_remaining
                    == args
                        .seconds
                        .map(|s| s * args.rate as u64)
                        .unwrap_or(u64::MAX)
                {
                    eprintln!("⚠️  ADVISORY: Triangle (DPW1 Naive) is non-band-limited and will alias at high frequencies.");
                }
                signal_pipe::<TriangleDPW1>(
                    &mut osc,
                    &phases[0..chunk_size],
                    &gain,
                    &mut buffer[0..chunk_size],
                );
            }
            ShapeArg::Pulse => {
                osc.duty = Scalar::from_num(0.1); // Default 10% duty
                signal_pipe::<Pulse>(
                    &mut osc,
                    &phases[0..chunk_size],
                    &gain,
                    &mut buffer[0..chunk_size],
                );
            }
        }

        // Safe Serialization: Convert i32 buffer to Little-Endian bytes
        let mut byte_buffer = [0u8; 512 * 4];
        for (i, &sample) in buffer[0..chunk_size].iter().enumerate() {
            byte_buffer[i * 4..(i + 1) * 4].copy_from_slice(&sample.to_le_bytes());
        }

        if let Err(e) = handle.write_all(&byte_buffer[0..chunk_size * 4]) {
            if e.kind() == io::ErrorKind::BrokenPipe {
                break;
            }
            return Err(e);
        }

        if args.seconds.is_some() {
            samples_remaining -= chunk_size as u64;
        }
    }
    handle.flush()?;
    Ok(())
}

fn write_wav_header<W: Write>(writer: &mut W, sample_rate: u32, data_bytes: u32) -> io::Result<()> {
    let total_file_size = 36 + data_bytes;
    writer.write_all(b"RIFF")?;
    writer.write_all(&total_file_size.to_le_bytes())?;
    writer.write_all(b"WAVE")?;
    writer.write_all(b"fmt ")?;
    writer.write_all(&16u32.to_le_bytes())?; // Chunk size
    writer.write_all(&1u16.to_le_bytes())?; // AudioFormat (PCM)
    writer.write_all(&1u16.to_le_bytes())?; // NumChannels (Mono)
    writer.write_all(&sample_rate.to_le_bytes())?;
    let byte_rate = sample_rate * 4;
    writer.write_all(&byte_rate.to_le_bytes())?;
    writer.write_all(&4u16.to_le_bytes())?; // BlockAlign
    writer.write_all(&(BIT_DEPTH_32 as u16).to_le_bytes())?; // BitsPerSample
    writer.write_all(b"data")?;
    writer.write_all(&data_bytes.to_le_bytes())?;
    Ok(())
}

// --- COMMAND: INSPECT ---
fn run_inspect(file_path: Option<PathBuf>) -> io::Result<()> {
    let mut reader: Box<dyn Read> = match file_path {
        Some(path) => Box::new(File::open(path)?),
        None => Box::new(io::stdin()),
    };

    let mut header_bytes = [0u8; HEADER_SIZE];
    reader.read_exact(&mut header_bytes).map_err(|e| {
        io::Error::new(
            io::ErrorKind::UnexpectedEof,
            format!("Failed to read DP32 header: {}", e),
        )
    })?;

    if let Err(e) = HeaderVerifier::verify_frame_exact(&header_bytes) {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            format!("❌ Invalid Header: {}", e),
        ));
    }

    let version = u32::from_le_bytes(
        header_bytes[4..8]
            .try_into()
            .map_err(|_| io::Error::new(io::ErrorKind::InvalidData, "Invalid version field"))?,
    );
    let sequence = u64::from_le_bytes(
        header_bytes[8..16]
            .try_into()
            .map_err(|_| io::Error::new(io::ErrorKind::InvalidData, "Invalid sequence field"))?,
    );
    let rate = u32::from_le_bytes(
        header_bytes[16..20]
            .try_into()
            .map_err(|_| io::Error::new(io::ErrorKind::InvalidData, "Invalid rate field"))?,
    );
    let depth = u32::from_le_bytes(
        header_bytes[20..24]
            .try_into()
            .map_err(|_| io::Error::new(io::ErrorKind::InvalidData, "Invalid depth field"))?,
    );

    eprintln!("=== DP32 Inspector ===");
    eprintln!("Status:      VALID HEADER");
    eprintln!("Protocol v:  {}", version);
    eprintln!("Sample Rate: {} Hz", rate);
    eprintln!("Bit Depth:   {}-bit (S32LE)", depth);
    eprintln!("Sequence:    {}", sequence);
    Ok(())
}

// --- COMMAND: VERIFY ---
fn run_verify(file_path: PathBuf) -> Result<(), VerifyError> {
    let mut file = File::open(&file_path)?;
    let file_size = file.metadata()?.len();

    if file_size < HEADER_SIZE as u64 {
        return Err(VerifyError::Parse("❌ FAIL: File too small".to_string()));
    }

    let mut header_bytes = [0u8; HEADER_SIZE];
    file.read_exact(&mut header_bytes)?;

    match HeaderVerifier::verify_frame_exact(&header_bytes) {
        Ok(_) => {}
        Err(e) => match e {
            VerificationError::ChecksumMismatch | VerificationError::ReservedBytesNotEmpty => {
                return Err(VerifyError::Integrity(format!("❌ FAIL: {}", e)));
            }
            _ => return Err(VerifyError::Parse(format!("❌ FAIL: {}", e))),
        },
    }

    let rate = u32::from_le_bytes(
        header_bytes[16..20]
            .try_into()
            .map_err(|_| VerifyError::Parse("Invalid rate in header".to_string()))?,
    );
    let depth = u32::from_le_bytes(
        header_bytes[20..24]
            .try_into()
            .map_err(|_| VerifyError::Parse("Invalid depth in header".to_string()))?,
    );

    if depth != BIT_DEPTH_32 {
        return Err(VerifyError::Parse(format!(
            "❌ FAIL: Invalid Depth ({})",
            depth
        )));
    }

    let payload = file_size - HEADER_SIZE as u64;
    if payload % 4 != 0 {
        return Err(VerifyError::Parse(format!(
            "❌ FAIL: Misaligned Payload ({} bytes)",
            payload
        )));
    }

    let duration = (payload / 4) as f64 / rate as f64;
    println!("✅ VERIFIED: DP32 Reference File");
    println!("   Duration: {:.4} sec", duration);
    Ok(())
}
