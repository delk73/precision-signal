fn main() -> Result<(), Box<dyn std::error::Error>> {
    let cli = Cli::parse();

    #[cfg(feature = "audit")]
    if cli.audit {
        dpw4::reset_audit_counters();
    }

    let command = cli
        .command
        .unwrap_or_else(|| Commands::Generate(GenerateArgs::parse_from(std::env::args())));

    let result: Result<(), Box<dyn std::error::Error>> = match command {
        Commands::Generate(args) => run_generate(args).map_err(|e| e.into()),
        Commands::Artifacts(args) => run_artifacts(args).map_err(|e| e.into()),
        Commands::Validate(args) => {
            let exit_code = run_validate(args);
            std::process::exit(exit_code);
        }
        Commands::Inspect { file } => run_inspect(file).map_err(|e| e.into()),
        Commands::Verify { file } => match run_verify(file) {
            Ok(()) => Ok(()),
            Err(VerifyError::Integrity(msg)) => {
                // Exit contract: integrity failure (checksum/reserved) → exit(2); all other errors → exit(1).
                eprintln!("Error: {}", msg);
                std::process::exit(2);
            }
            Err(e) => Err(e.into()),
        },

    };

    #[cfg(feature = "audit")]
    if cli.audit {
        // bitlen = 128 - leading_zeros(abs(z))
        eprintln!("AUDIT: Max |z| bitlen: {}", dpw4::max_abs_z_bits());
        eprintln!(
            "AUDIT: Legacy Shift Overflow Risk: {}",
            dpw4::legacy_shift_overflow_risk()
        );
        eprintln!(
            "AUDIT: Integrator Near Overflow:   {}",
            dpw4::integrator_near_overflow()
        );
    }

    if let Err(e) = result {
        eprintln!("Error: {}", e);
        std::process::exit(1);
    }

    Ok(())
}

