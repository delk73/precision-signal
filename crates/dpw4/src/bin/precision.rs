struct Dpw4StateF64 {
    z1: f64,
    z2: f64,
    z3: f64,
}

impl Dpw4StateF64 {
    fn new() -> Self {
        Self {
            z1: 0.0,
            z2: 0.0,
            z3: 0.0,
        }
    }

    fn reset(&mut self) {
        self.z1 = 0.0;
        self.z2 = 0.0;
        self.z3 = 0.0;
    }
}

fn tick_dpw4_f64(state: &mut Dpw4StateF64, phase_01: f64) -> f64 {
    let s = (phase_01 * 2.0) - 1.0;
    let p4 = s * s * s * s;
    let d1 = p4 - state.z1;
    state.z1 = p4;
    let d2 = d1 - state.z2;
    state.z2 = d1;
    let d3 = d2 - state.z3;
    state.z3 = d2;
    d3 * C4
}

